function createfigure_Map_PMI_8x8(image, map, data, lims)
% this function is only meaningful in combination with the Chan_Layout_ER.jpg image
% this function overlays the channel positions on that image with colored
% circles
% INPUT
% image: image created based on Chan_Layout_ER.jpg 
% map:   a color map
% data:  data stored in the correct order in a matrix storing the channel
%        layout
% lims:  ploting limits
% pruned_chans: boolean vector - 0 if channel signal quality is bad, i.e.
